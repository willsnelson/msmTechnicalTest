package com.moneysupermarkettest.technical.framework;

public class PriceRange {

	private double from;

	private double to;

	/**
	 * @return the from
	 */
	public double getFrom() {
		return from;
	}

	/**
	 * @param from
	 *            the from to set
	 */
	public void setFrom(double from) {
		this.from = from;
	}

	/**
	 * @return the to
	 */
	public double getTo() {
		return to;
	}

	/**
	 * @param to
	 *            the to to set
	 */
	public void setTo(double to) {
		this.to = to;
	}

}
